package com.oono.exer3;
/*
 * 定义一个Circle类，声明radius属性，提供getter/setter
 * 
 */
public class Circle {
	
	private double radius;
	
	public Circle(){
		
	}
	public Circle(double radius){
		this.radius = radius;
	}
	
	public double getRadius(){
		return radius;
	}
	public void setRadius(double radius){
		this.radius = radius;
	}

	
	
}
